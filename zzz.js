function togglecp(){
	var cp =document.getElementById('cp');
	cp.style.height= window.innerHeight -200 + 'px';
	if(cp.style.right = '0px')
	{cp.style.right = '1000px';
	
		}
	else{cp.style.right= '0px';}	
		
		}



